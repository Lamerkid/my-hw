func TestTop10(t *testing.T) {
	t.Run("no words in empty string", func(t *testing.T) {
		require.Len(t, Top10(""), 0)
	})

	t.Run("positive test", func(t *testing.T) {
		if taskWithAsteriskIsCompleted {
			expected := []string{
				"а",         // 8
				"он",        // 8
				"и",         // 6
				"ты",        // 5
				"что",       // 5
				"в",         // 4
				"его",       // 4
				"если",      // 4
				"кристофер", // 4
				"не",        // 4
			}
			require.Equal(t, expected, Top10(text))
		} else {
			expected := []string{
				"он",        // 8
				"а",         // 6
				"и",         // 6
				"ты",        // 5
				"что",       // 5
				"-",         // 4
				"Кристофер", // 4
				"если",      // 4
				"не",        // 4
				"то",        // 4
			}
			require.Equal(t, expected, Top10(text))
		}
		if taskWithAsteriskIsCompleted {
			expected := []string{
				"his",       // 7
				"the",       // 5
				"-------",   // 3
				"from",      // 3
				"in",        // 3
				"abc...cba", // 2
				"age",       // 2
				"and",       // 2
				"looks",     // 2
				"on",        // 2
			}
			require.Equal(t, expected, Top10(text2))
		} else {
			expected := []string{
				"his",       // 7
				"-",         // 5
				"the",       // 4
				"-------",   // 3
				"in",        // 3
				"abc...cba", // 2
				"age,",      // 2
				"from",      // 2
				"looks",     // 2
				"on",        // 2
			}
			require.Equal(t, expected, Top10(text2))
		}
	})
}
